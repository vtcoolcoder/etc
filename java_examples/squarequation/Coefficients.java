//package squarequation;

class Coefficients {
    private double a;
    private double b;
    private double c;
    
    Coefficients(double a, double b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }
    
    double getA() { return a; }
    double getB() { return b; }
    double getC() { return c; }
}